void main()
    {
        int n = 2;
        switch (n)
        {
            case 1: {
                print(" The number is 1");
            }
            break;
            
            case 2: {
                print(" The number is 2");
            }
            break;
            
            case 3: {
                print(" The number is 3");
            }
            break;
            
            default: {
                print("This is default case");
            }
            break;
        }
    }

void main()
{
    int a = 10;
    int b = 20;
    
    print("sum = ${a+b}");  // Addition
    print("sub = ${a-b}");  // Subtraction
    print("div = ${a/b}");  // Division
    print("mult = ${a*b}");  // Multiply
    print("reminder = ${a%b}");  // Modulus

}
/* 
output:
sum = 30
sub = -10
div = 0.5
mult = 200
reminder = 10 
